def seed():
    with get_cursor() as cur:
        # Upsert themes
        for name, category, description in THEMES:
            cur.execute("""
                INSERT INTO tw.themes (name, category, description)
                VALUES (%s, %s, %s)
                ON CONFLICT (name) DO UPDATE SET
                    category = EXCLUDED.category,
                    description = EXCLUDED.description,
                    updated_at = NOW()
            """, (name, category, description))

        # Upsert stock-theme mappings
        for theme_name, stock_ids in MAPPINGS.items():
            cur.execute("SELECT theme_id FROM tw.themes WHERE name = %s", (theme_name,))
            row = cur.fetchone()
            if not row:
                print(f"  [SKIP] Theme not found: {theme_name}")
                continue
            theme_id = row["theme_id"]

            for stock_id in stock_ids:
                # Only insert if stock exists in DB
                cur.execute("SELECT 1 FROM tw.stocks WHERE stock_id = %s", (stock_id,))
                if not cur.fetchone():
                    continue
                cur.execute("""
                    INSERT INTO tw.stock_themes (stock_id, theme_id)
                    VALUES (%s, %s)
                    ON CONFLICT (stock_id, theme_id) DO NOTHING
                """, (stock_id, theme_id))

        # Summary
        cur.execute("SELECT COUNT(*) AS cnt FROM tw.themes")
        theme_count = cur.fetchone()["cnt"]
        cur.execute("SELECT COUNT(*) AS cnt FROM tw.stock_themes")
        mapping_count = cur.fetchone()["cnt"]
        print(f"Seeded {theme_count} themes, {mapping_count} stock-theme mappings.")


if __name__ == "__main__":
    seed()
